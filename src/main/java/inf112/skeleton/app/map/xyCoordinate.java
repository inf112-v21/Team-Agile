package inf112.skeleton.app.map;

public class xyCoordinate {
    public xyCoordinate(float x, float y){
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    float x;

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    float y;
}
